class Operators
{
public static void main(String args[])
{
System.out.println(3&6);

System.out.println(3|6);


System.out.println((3<6)&(5>2));

System.out.println((3<6)&(5<2));

System.out.println((3<6)|(5>2));

System.out.println((3<6)|(5<2));

System.out.println((3<6)&&(5>2));

System.out.println((3<6)&&(5<2));

System.out.println((3<6)||(5>2));

System.out.println((3<6)||(5<2));


}
}
