class Literal
{
public static void main(String args[])
{
System.out.println(11090);//integer literal=11090

System.out.println(053);//octal literal starts with 0 =43

System.out.println(0xa2);//hexadcimal literal starts with 0x =162

System.out.println(9.8);//floating literal

System.out.println(9.8e2);//exponential literal=980

System.out.println(3+5);//boolean literal =8

System.out.println(8);//integer literal=8

System.out.println(3>5);//boolean literal =false

System.out.println('#');//character literal=#

System.out.println(true);//boolean literal=true

System.out.println("hello");//string literal=hello

System.out.println('\u0042');//character literal=B

}
}
